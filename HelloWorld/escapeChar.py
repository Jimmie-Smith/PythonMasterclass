splitString = "This string has been \nsplit over \nseveral \nlines"
print(splitString)
tabbedString = "1\t2\t3\t4\t5"
print(tabbedString)
print('the petshop owner said "no, no, \'e \' uh... he\'s resting".')

# If you enclosed the string with a ', the rest of the ' must be escaped. Or just used triple quotes for better readability

print("""The shop owner said "No, no 'e's uh, .... he's resting".""""")

# You can also use triple quotes if you want to break lines instead of using \n like in the first example

print("""The shop owner said
"No, no 'e's uh, .... 
he's resting".""""")